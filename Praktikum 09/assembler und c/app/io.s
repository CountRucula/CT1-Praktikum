				AREA myCode, CODE, READONLY
                
		

; -------------------------------------------------------------------
; -- Functions
; -------------------------------------------------------------------


; -------------------------------------------------------------------
; void write(uint32_t address, uint32_t value)
; -------------------------------------------------------------------
; R0 -> address
; R1 -> value
write			PROC
				EXPORT 	write
				STR		R1, [R0]
				BX		LR
				
				ALIGN
				ENDP
		
; -------------------------------------------------------------------
; uint32_t read(uint32_t address)
; -------------------------------------------------------------------
; R0 -> address
; R0 -> return value
read			PROC
				EXPORT 	read
				LDR		R0, [R0]
				BX		LR
				
				ALIGN
				ENDP
					
; -------------------------------------------------------------------
; -- End of file
; -------------------------------------------------------------------   
				END
		